
# Write a script that takes two numbers as input and performs addition, subtraction, multiplication, and division.

def add(x, y):
    return x + y

def subtract(x, y): 
    return x - y 

def multiply(x, y):
    return x * y

def divide(x, y):
    return x / y 

num1 = 50 
num2 = 10

print("Addition:", add(num1, num2))
print("Subtraction:", subtract(num1, num2))
print("Multiplication:", multiply(num1, num2))
print("Division:", divide(num1, num2))

